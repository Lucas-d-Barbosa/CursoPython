# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número...
# ...positivo ou negativo. Int sem sinal é considerado... 
# ...positivo

print(11) # int
print(-11) # int
print(0) # int


# Tipos int e float
# Float -> Número com pontos flutuantes
# O tipo int representa qualquer número...
# ...positivo ou negativo com ponto flutuante. 
# float sem sinal é considerado... 
# ...positivo

print(1.1, 10.11)
print(0.0 -1.5)

# A função type mostra o tipo que o Python 
# ... Inferiu ao valor.

print( type(10.0), type("Lucas Barbosa"), type(10) )